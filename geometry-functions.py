def triange_surface(base, height):
    return base * height / 2

def square_perimetre(side):
    return 4 * side

def square_surface(side):
    return side ** 2

def circle_perimetre(radious):
    pi = 3.14159
    return 2 * pi * radious

def circle_surface(radious):
    pi = 3.14159
    return pi * (radious ** 2)

